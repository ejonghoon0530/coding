#--------------------------drop()--------------------------------------
import pandas as pd

exam_data = {'수학' : [90, 80, 70], '영어' : [98, 89, 95], '음악' : [85, 95, 100], '체육' : [100, 90, 90]}

df = pd.DataFrame(exam_data, index = ['서준', '우현', '인아'])
print(df)
print('\n')

df2 = df[:] #df를 복사해서 df2에 저장
df2.drop('우현', inplace = True) #우현이라는 행 삭제
print(df2)
print('\n')

df3 = df[:] #df를 복사해서 df3에 저장
df3.drop(['우현', '인아'], axis = 0, inplace = True) #우현과 인아라는 행 삭제
print(df3)
print('\n')

# drop 함수의 axis는 0이 기본값
# axis = 0은 행을 삭제
# axis = 1은 열을 삭제

