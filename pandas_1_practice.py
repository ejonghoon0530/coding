#--------------------------rename()----------------------------

import pandas as pd

df = pd.DataFrame([[15, '남', '덕영중'], [17, '여', '수리중']],
                   index = ['준서', '예은'],
                   columns = ['나이', '성별', '학교'])

print(df)
print('\n')                  

df.rename(columns = {'나이' : '연령', '성별' : '남녀', '학교' : '소속'}, inplace = True)
df.rename(index = {'준서' : '학생1', '예은' : '학생2'}, inplace = True)
print(df)
print('\n')
#'바꾸려는 열이름' : '바꿀 열이름'
# inplace = True는 원본에 적용하겠다는 뜻
# False면 원본은 그대로고 바뀐 데이터프레임이 새로 만들어짐
# False가 기본값임

